[<Fact>]
let string () =
    "\"\"" |> rep |> should equal "\"\""
    "\"12345\"" |> rep |> should equal "\"12345\""
    "\"hello\\\"world\"" |> rep |> should equal "\"hello\\\"world\""
    "\"\\x3a;\"" |> rep |> should equal "\":\""
    "\"\\X3A;\"" |> rep |> should equal "\":\""
    "\"\\x003A;\"" |> rep |> should equal "\":\""
    "\"\\x3071;\"" |> rep |> should equal "\"ぱ\""
    "\"\\x1F600;\"" |> rep |> should equal "\"😀\""
    "\"foo\\ \n  bar\"" |> rep |> should equal "\"foobar\""
    "\"a\\vb\"" |> rep |> should equal "\"a\u000bb\""
    "\"a\\fb\"" |> rep |> should equal "\"a\u000cb\""

    "\"1\\a2\\b3\\t4\\n5\\r6\\\"7\\\\8|90\""
    |> rep
    |> should equal "\"1\a2\b3\t4\n5\r6\\\"7\\8|90\""

[<Fact>]
let vector () =
    "#()" |> rep |> should equal "#()"
    "#(1 2 3)" |> rep |> should equal "#(1 2 3)"
    "#(0 10 5)" |> rep |> should equal "#(0 10 5)"
    "#(\"a\" \"b\" \"c\")" |> rep |> should equal "#(\"a\" \"b\" \"c\")"
    "#(a b c)" |> rep |> should equal "#(a b c)"
    "'#(1 2)" |> rep |> should equal "#(1 2)"
    "#(  1   2   )" |> rep |> should equal "#(1 2)"
    "#(1 #(2) 3)" |> rep |> should equal "#(1 #(2) 3)"
    "#(1 'a 2)" |> rep |> should equal "#(1 'a 2)"

[<Fact>]
let bytevector () =
    "#u8()" |> rep |> should equal "#u8()"
    "#u8(1 2 3)" |> rep |> should equal "#u8(1 2 3)"
    "#u8(0 10 5)" |> rep |> should equal "#u8(0 10 5)"
    "#u8(255)" |> rep |> should equal "#u8(255)"
    "#u8(#x10 #o10 #b10)" |> rep |> should equal "#u8(16 8 2)"
    "'#u8(1 2)" |> rep |> should equal "#u8(1 2)"
    "#U8(1 2)" |> rep |> should equal "#u8(1 2)"
    "#u8(  1   2   )" |> rep |> should equal "#u8(1 2)"
    (fun () -> "#u8(256)" |> rep |> ignore) |> should throw typeof<System.Exception>
    (fun () -> "#u8(-1)" |> rep |> ignore) |> should throw typeof<System.Exception>
    (fun () -> "#u8(1.0)" |> rep |> ignore) |> should throw typeof<System.Exception>

    (fun () -> "#u8(1+2i)" |> rep |> ignore)
    |> should throw typeof<System.Exception>

[<Fact>]
let ``comments`` () =
    "; comment\n1" |> rep |> should equal "1"
    "#| block comment |# 42" |> rep |> should equal "42"
