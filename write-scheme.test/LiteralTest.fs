    "#false" |> rep |> should equal "#f"
    "#FALSE" |> rep |> should equal "#f"

[<Fact>]
let ``pair and list`` () =
    "'()" |> rep |> should equal "()"
    "'(1 2 3)" |> rep |> should equal "(1 2 3)"
    "'(1 (2) 3)" |> rep |> should equal "(1 (2) 3)"
    "'(1 . 2)" |> rep |> should equal "(1 . 2)"
    "'(1 2 . 3)" |> rep |> should equal "(1 2 . 3)"
    "'(1 2 . (3 4))" |> rep |> should equal "(1 2 3 4)"
    "'(1 . (2 . (3 . ())))" |> rep |> should equal "(1 2 3)"
    "'(1 . (2 . 3))" |> rep |> should equal "(1 2 . 3)"
    "'(1 2 . (3 . (4 . ())))" |> rep |> should equal "(1 2 3 4)"
    "'(1 #t \"a\")" |> rep |> should equal "(1 #t \"a\")"
    "'(a 'b)" |> rep |> should equal "(a 'b)"
    "'(  1   2  )" |> rep |> should equal "(1 2)"
    "'`(a ,b ,@c)" |> rep |> should equal "`(a ,b ,@c)"

