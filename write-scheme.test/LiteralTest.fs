    "1.5-2.5i" |> rep |> should equal "1.5-2.5i"
    "1+i" |> rep |> should equal "1+1i"
    "1-i" |> rep |> should equal "1-1i"
    "2i" |> rep |> should equal "0+2i"
    "-2.5i" |> rep |> should equal "0-2.5i"
    "1/2+3/4i" |> rep |> should equal "0.5+0.75i"
    "+i" |> rep |> should equal "0+1i"
    "-i" |> rep |> should equal "0-1i"
