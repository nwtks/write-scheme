    "-nan.0" |> rep |> should equal "+nan.0"
    "+inf.0" |> rep |> should equal "+inf.0"
    "+Inf.0" |> rep |> should equal "+inf.0"
    "-inf.0" |> rep |> should equal "-inf.0"
    "-INF.0" |> rep |> should equal "-inf.0"
    "1/2" |> rep |> should equal "1/2"
    "1/3" |> rep |> should equal "1/3"
    "3/6" |> rep |> should equal "1/2"
    "10/2" |> rep |> should equal "5"
