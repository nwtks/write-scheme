    "(equal? #u8(1 2 3) #u8(1 2 4))" |> rep |> should equal "#f"
    "(equal? #u8() #u8())" |> rep |> should equal "#t"
    "(equal? #u8(1) #u8(1 2))" |> rep |> should equal "#f"
    "(equal? #u8(1 2) #u8(1 2 3))" |> rep |> should equal "#f"
