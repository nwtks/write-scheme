    "(equal? \"a\" \"abc\")" |> rep |> should equal "#f"
    "(equal? 1 \"1\")" |> rep |> should equal "#f"
    "(equal? '#(1 2 3) '#(1 2 3))" |> rep |> should equal "#t"
    "(equal? '#(1 2 3) '#(1 2 4))" |> rep |> should equal "#f"
    "(equal? '#(1 2) '#(1 2 3))" |> rep |> should equal "#f"
