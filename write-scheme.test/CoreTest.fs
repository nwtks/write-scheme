    "(equal? (values 1 2) (values 1 2 3))" |> rep |> should equal "#f"
    "(let ((p \"a\")) (equal? p p))" |> rep |> should equal "#t"
    "(equal? '() '())" |> rep |> should equal "#t"
    "(equal? (cons 1 2) (cons 1 2))" |> rep |> should equal "#t"
    "(equal? (lambda () 1) (lambda () 1))" |> rep |> should equal "#f"
