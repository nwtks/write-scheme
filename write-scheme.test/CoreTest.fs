module WriteScheme.Tests.CoreTest

open Xunit
open FsUnit.Xunit

let rep = WriteScheme.Repl.rep WriteScheme.Builtin.builtin

[<Fact>]
let ``eqv?`` () =
    "(eqv? 'a 'a)" |> rep |> should equal "#t"
    "(eqv? 'a 'b)" |> rep |> should equal "#f"
    "(eqv? ''a ''a)" |> rep |> should equal "#t"
    "(eqv? ''a ''b)" |> rep |> should equal "#f"
    "(eqv? ',1 ',1)" |> rep |> should equal "#t"
    "(eqv? ',1 ',2)" |> rep |> should equal "#f"
    "(eqv? 2 2)" |> rep |> should equal "#t"
    "(eqv? 2 3)" |> rep |> should equal "#f"
    "(eqv? 1/3 1/3)" |> rep |> should equal "#t"
    "(eqv? 1/3 2/3)" |> rep |> should equal "#f"
    "(eqv? 1/3 1/2)" |> rep |> should equal "#f"
    "(eqv? 2/4 1/2)" |> rep |> should equal "#t"
    "(eqv? 4/2 2)" |> rep |> should equal "#t"
    "(eqv? 1.5 1.5)" |> rep |> should equal "#t"
    "(eqv? 1.5 2.5)" |> rep |> should equal "#f"
    "(eqv? 3/2 1.5)" |> rep |> should equal "#f"
    "(eqv? 1+2i 1+2i)" |> rep |> should equal "#t"
    "(eqv? 1+2i 1+3i)" |> rep |> should equal "#f"
    "(eqv? 1.3-2.5i 1.3-2.5i)" |> rep |> should equal "#t"
    "(eqv? 1.3+0i 1.3)" |> rep |> should equal "#f"
    "(eqv? #t #t)" |> rep |> should equal "#t"
    "(eqv? #t #f)" |> rep |> should equal "#f"
    "(eqv? #f 'nil)" |> rep |> should equal "#f"
    "(eqv? '(a) '(a))" |> rep |> should equal "#f"
    "(eqv? '(a (b) c) '(a (b) c))" |> rep |> should equal "#f"
    "(eqv? '(1 . 2) '(1 . 2))" |> rep |> should equal "#f"
    "(eqv? '(1 2 . 3) '(1 2 . 3))" |> rep |> should equal "#f"
    "(eqv? #\\a #\\a)" |> rep |> should equal "#t"
    "(eqv? #\\a #\\b)" |> rep |> should equal "#f"
    "(eqv? \"abc\" \"abc\")" |> rep |> should equal "#f"
    "(eqv? 1 \"1\")" |> rep |> should equal "#f"
    "(eqv? '#(1 2 3) '#(1 2 3))" |> rep |> should equal "#f"
    "(eqv? #u8(1 2 3) #u8(1 2 3))" |> rep |> should equal "#f"
    "(eqv? (values 1 2) (values 1 2))" |> rep |> should equal "#f"
    "(let ((p \"a\")) (eqv? p p))" |> rep |> should equal "#t"
    "(eqv? '() '())" |> rep |> should equal "#t"
    "(eqv? (cons 1 2) (cons 1 2))" |> rep |> should equal "#f"
    "(eqv? (lambda () 1) (lambda () 1))" |> rep |> should equal "#f"
    "(let ((p (lambda (x) x))) (eqv? p p))" |> rep |> should equal "#t"

